class Solution{
    public:
    //Function to return the count of number of elements in union of two arrays.
    int doUnion(int a[], int n, int b[], int m)  {
        //code here
        set<int> res;
        
        for(int i=0; i<n; i++){
            res.insert(a[i]);
        }
        
        for(int i=0; i<m; i++){
            res.insert(b[i]);
        }
        
        return res.size();
    }
};
